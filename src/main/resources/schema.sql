CREATE TABLE IF NOT EXISTS mpa (
    id INTEGER PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS genres (
    id INTEGER PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS films (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    description VARCHAR(500),
    duration INTEGER,
    releaseDate DATE,
    mpaId INTEGER REFERENCES mpa,
    genreId INTEGER REFERENCES genres
);

CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(50) NOT NULL,
    login VARCHAR(50) NOT NULL,
    name VARCHAR(50),
    birthday DATE
);

CREATE TABLE IF NOT EXISTS likes_films (
    film_id INTEGER REFERENCES films ON DELETE CASCADE,
    user_id INTEGER REFERENCES users ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS friendship (
    user_id INTEGER REFERENCES users ON DELETE CASCADE,
    friend_id INTEGER REFERENCES users ON DELETE CASCADE,
    status VARCHAR(10)
);


